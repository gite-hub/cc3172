const int cSki::cGap = 10;

bool cSki::toDescs(eSki eski, sDescs& descs)
{
	descs.clear();
	if (eski == eSki::eNone)
	{
		return false;
	}
	const auto& icon = txtIcon::getIcons().at(eIcon::ski).at((int)eski);
	descs.name = icon.name;
	descs.uhead = icon.uid;

	sDesc d;
	if (!icon.desc.empty())
	{
		d.text = icon.desc;
		d.color = s888::WHITE;
		descs.push(d);
	}
	const auto& skis = txtSki::getSkis();
	const auto& it = skis.find(eski);
	if (it == skis.end())
	{
		return true;
	}
	const auto& costs = it->second.descCost;
	if (costs.empty())
	{
		return true;
	}
	d.text = cc8::cCost + cc8::cColon + costs;
	d.color = s888::RED;
	descs.push(d);
	return true;
}


